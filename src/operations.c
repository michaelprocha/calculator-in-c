#include "../include/calculator.h"

float addition(float n1, float n2)
{
    float result = n1 + n2;
    return result;
}

float subtraction(float n1, float n2)
{
    float result = n1 - n2;
    return result;
}

float multiplication(float n1, float n2)
{
    float result = n1 * n2;
    return result;
}

float division(float n1, float n2)
{
    float result = n1 / n2;
    return result;
}